@partners_bp.route('/api/admin/partners/<guid>', methods=['PATCH'])
@require_auth
@require_su
def admin_edit_partner(guid: str):
    """Edit mutable fields. Cannot change country_code, org_number,
    auth_kind — those changes require revoke + re-register (plan §5.3)."""
    cleaned, err = _validate_payload(request.get_json(silent=True) or {}, creating=False)
    if err:
        return jsonify({'error': 'invalid', 'message': err}), 400

    immutable_attempted = [k for k in ('country_code', 'org_number', 'auth_kind')
                          if k in cleaned]
    if immutable_attempted:
        return jsonify({
            'error': 'invalid',
            'message': f"cannot change {immutable_attempted} after creation; "
                       f"revoke and re-register",
        }), 400

    session = get_db()
    p = session.query(ExternalPartner).filter_by(guid=guid).first()
    if p is None:
        return jsonify({'error': 'not_found'}), 404

    before = p.admin_dict()
    for k, v in cleaned.items():
        setattr(p, k, v)
    p.updated_at = _now()
    _record_audit(session, p, 'edited', before, p.admin_dict(), g.current_user.guid)
    session.commit()
    audit('partner.edited', user_guid=g.current_user.guid,
          detail={'partner_guid': p.guid})
    return jsonify(p.admin_dict()), 200


@partners_bp.route('/api/admin/partners/<guid>/rotate', methods=['POST'])
@require_auth
@require_su
def admin_rotate_credential(guid: str):
    """Rotate the partner's credential. Old secret stops working immediately.
    New secret returned once."""
    session = get_db()
    p = session.query(ExternalPartner).filter_by(guid=guid).first()
    if p is None:
        return jsonify({'error': 'not_found'}), 404
    if p.auth_kind == 'none':
        return jsonify({'error': 'invalid', 'message': 'partner has no credential'}), 400

    before = p.admin_dict()
    cleartext = _generate_secret()
    if p.auth_kind == 'api_key':
        p.api_key_hash = hash_password(cleartext)
    else:
        p.client_secret_hash = hash_password(cleartext)
    p.last_rotated_at = _now()
    p.updated_at = p.last_rotated_at
    _record_audit(session, p, 'rotated', before, p.admin_dict(), g.current_user.guid)
    session.commit()
    audit('partner.rotated', user_guid=g.current_user.guid,
          detail={'partner_guid': p.guid})

    return jsonify({
        **p.admin_dict(),
        'secret_cleartext': cleartext,
        'secret_warning': (
            "Old credential is now invalid. New secret will not be shown "
            "again — copy it now and deliver to the partner securely."
        ),
    }), 200


