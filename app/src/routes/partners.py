@partners_bp.route('/api/internal/partner/<guid>/validate', methods=['POST'])
@require_service_key
def validate_partner(guid: str):
    """Verify a partner credential and return the access blob.

    Body: ``{"secret": "<api_key_or_client_secret>"}``

    Returns:
      200 with ``{access_blob: {...}}`` if valid + active + unexpired
      401 if invalid
      403 if suspended / revoked / expired
      404 if no such partner
    """
    body = request.get_json(silent=True) or {}
    secret = body.get('secret', '')
    if not secret:
        return jsonify({'error': 'missing_secret'}), 400

    session = get_db()
    p = session.query(ExternalPartner).filter_by(guid=guid).first()
    if p is None:
        return jsonify({'error': 'not_found'}), 404

    if p.status != 'active':
        return jsonify({'error': 'forbidden', 'reason': p.status}), 403

    if p.expires_at and p.expires_at < _now():
        return jsonify({'error': 'forbidden', 'reason': 'expired'}), 403

    stored = p.client_secret_hash if p.auth_kind == 'oauth_client' else p.api_key_hash
    if not stored or not verify_password(secret, stored):
        return jsonify({'error': 'unauthorized'}), 401

    blob = {
        'user_type': 'partner',
        'partner_guid': p.guid,
        'display_name': p.display_name,
        'allowed_scopes': p.allowed_scopes or [],
        'allowed_services': p.allowed_services or [],
        'allowed_org_guids': p.allowed_org_guids,
        'is_su_admin': False,
        'organization_ids': p.allowed_org_guids or [],
    }
    return jsonify({'access_blob': blob}), 200


