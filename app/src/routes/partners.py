@partners_bp.route('/api/admin/partners/<guid>/revoke', methods=['POST'])
@require_auth
@require_su
def admin_revoke(guid: str):
    """Irreversible at the SSO layer — registering again requires a new GUID."""
    body = request.get_json(silent=True) or {}
    reason = body.get('reason') or ''
    session = get_db()
    p = session.query(ExternalPartner).filter_by(guid=guid).first()
    if p is None:
        return jsonify({'error': 'not_found'}), 404
    if p.status == 'revoked':
        return jsonify({'error': 'invalid', 'message': 'already revoked'}), 400
    before = p.admin_dict()
    p.status = 'revoked'
    p.revoked_at = _now()
    p.revoke_reason = reason
    p.updated_at = p.revoked_at
    _record_audit(session, p, 'revoked', before, p.admin_dict(), g.current_user.guid)
    session.commit()
    audit('partner.revoked', user_guid=g.current_user.guid,
          detail={'partner_guid': p.guid, 'reason': reason})
    return jsonify(p.admin_dict()), 200


