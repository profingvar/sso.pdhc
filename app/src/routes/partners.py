"""External Partner routes — public lookup, internal validation, SU admin CRUD.

Plan: ../../plans/external_partners_plan.md §4-§6.

Three groups of endpoints sharing the ``ExternalPartner`` model:

  Public  (no auth)         GET    /api/public/partner/<guid>
  Internal (service-key)    POST   /api/internal/partner/<guid>/validate
  Admin   (SU bearer token) GET    /api/admin/partners
                            POST   /api/admin/partners
                            GET    /api/admin/partners/<guid>
                            PATCH  /api/admin/partners/<guid>
                            POST   /api/admin/partners/<guid>/rotate
                            POST   /api/admin/partners/<guid>/suspend
                            POST   /api/admin/partners/<guid>/reactivate
                            POST   /api/admin/partners/<guid>/revoke
                            GET    /api/admin/partners/<guid>/audit

Credentials are generated by the server at create / rotate time, hashed
with the same scheme as user passwords, and shown to the SU exactly once
(in the response body of POST /partners or POST /partners/<guid>/rotate).
"""
